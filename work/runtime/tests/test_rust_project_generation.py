FIXTURE = Path(__file__).parent / "fixtures" / "flashdb-real-layout"


def _packet(root):
    return SimpleNamespace(metadata={"layout_resolution": {
        "source_dirs": [str(root / "src"), str(root / "inc")],
        "test_dirs": [str(root / "tests")],
    }})


def _legacy(root):
    return {
        "project_root": str(root), "public_apis": ["fdb_kv_set"], "test_files": ["tests/test_flashdb.c"],
        "functions": [
            {"name": "fdb_kv_set", "decl_kind": "prototype", "file": "inc/flashdb.h", "line": 16},
            {"name": "fdb_kv_set", "decl_kind": "definition", "file": "src/flashdb.c", "line": 10},
        ],
    }


def _inputs(trace):
    source = build_complete_analysis(_packet(FIXTURE), _legacy(FIXTURE))
    write_complete_analysis(source, trace)
    plan = build_semantic_plan(load_analysis_evidence(trace), run_id="planning-run")
    write_semantic_plan(plan, trace)
    return load_generation_inputs(trace)


def _project(root):
    project = root / "flashDB_rust"
    (project / "src").mkdir(parents=True)
    (project / "Cargo.toml").write_text(
        '[package]\nname = "flashdb-rust"\nversion = "0.1.0"\nedition = "2021"\nrust-version = "1.70"\n', encoding="utf-8")
    (project / "Cargo.lock").write_text(
        '# This file is automatically @generated by Cargo.\nversion = 3\n\n[[package]]\nname = "flashdb-rust"\nversion = "0.1.0"\n', encoding="utf-8")
    (project / "src" / "lib.rs").write_text(
        """#![forbid(unsafe_code)]

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum FdbStatus { Ok, Error }

pub trait StorageIo { fn read(&self, key: &str) -> Option<i32>; fn write(&mut self, key: &str, value: i32) -> bool; }
pub trait FlashPort { fn erase(&mut self, sector: usize) -> bool; }
pub trait ClockPort { fn now(&self) -> u64; }
pub trait LockPort { fn lock(&self) -> bool; fn unlock(&self); }

pub struct FdbBackend<W: FnMut(&str, i32) -> bool> { pub capacity: usize, pub writes: usize, pub write: W }

fn backend_is_valid<W: FnMut(&str, i32) -> bool>(backend: &FdbBackend<W>) -> bool {
    backend.capacity > 0 && backend.writes < backend.capacity
}

pub fn fdb_kv_set<W: FnMut(&str, i32) -> bool>(backend: &mut FdbBackend<W>, key: &str, value: i32) -> FdbStatus {
    if key.is_empty() || !backend_is_valid(backend) { return FdbStatus::Error; }
    if !(backend.write)(key, value) { return FdbStatus::Error; }
    backend.writes += 1;
    FdbStatus::Ok
}
""", encoding="utf-8")
    return project


class RustProjectGenerationTests(unittest.TestCase):
    def test_production_generation_has_no_benchmark_specific_branch(self):
        production = "\n".join(
            (RUNTIME / name).read_text(encoding="utf-8").lower()
            for name in ("rust_project_generation.py", "c2rust_project_generator.py")
        )
        for token in ("flashdb", "kvdb", "tsdb", "fdb_"):
            self.assertNotIn(token, production)

    def test_verified_chain_and_candidate_pass(self):
        with tempfile.TemporaryDirectory() as temp:
            root = Path(temp); trace = root / "trace"
            inputs = _inputs(trace); project = _project(root)
            result = validate_candidate(project, inputs)
        self.assertTrue(result["passed"], result)
        self.assertEqual(result["metrics"]["public_api_count"], 1)
        self.assertEqual(result["placeholder_findings"], [])
        self.assertEqual({item["source_symbol"] for item in result["implementation_map"]}, {"fdb_kv_set"})

    def test_loader_rejects_missing_stale_and_mixed_evidence(self):
        with tempfile.TemporaryDirectory() as temp:
            trace = Path(temp); _inputs(trace)
            doc = json.loads((trace / "behavior-contracts.json").read_text())
            doc["parent_analysis_run_id"] = "stale"
            (trace / "behavior-contracts.json").write_text(json.dumps(doc))
            with self.assertRaises(GenerationBlocked) as caught:
                load_generation_inputs(trace)
            self.assertTrue(any("planning:" in item or "mixed_planning_identity" in item for item in caught.exception.failures))
            (trace / "type-map.json").unlink()
            with self.assertRaises(GenerationBlocked) as caught:
                load_generation_inputs(trace)
            self.assertTrue(any("missing_analysis_evidence:type-map.json" in item for item in caught.exception.failures))

    def test_missing_api_and_function_are_not_removed_from_denominator(self):
        with tempfile.TemporaryDirectory() as temp:
            root = Path(temp); inputs = _inputs(root / "trace"); project = _project(root)
            source = project / "src" / "lib.rs"
            source.write_text(source.read_text().replace("pub fn fdb_kv_set", "pub fn omitted_set"))
            result = validate_candidate(project, inputs, build=False)
        self.assertFalse(result["checks"]["public_api_coverage_100_percent"])
        self.assertFalse(result["checks"]["unsupported_functions_empty"])

    def test_unresolved_generation_diagnostics_are_reported_at_final_gate(self):
        with tempfile.TemporaryDirectory() as temp:
            root = Path(temp); inputs = _inputs(root / "trace"); project = _project(root)
            unresolved = [{"symbol": "fdb_kv_set", "stage": "final_repair_pass", "reason": "no generic repair pattern matched", "resolved": False}]
            failed = validate_candidate(project, inputs, build=False, generation_diagnostics=unresolved)
            resolved = validate_candidate(project, inputs, build=False, generation_diagnostics=[{**unresolved[0], "resolved": True}])
        self.assertFalse(failed["checks"]["generation_diagnostics_clear"])
        self.assertIn("generation_diagnostics_clear", failed["failures"])
        self.assertEqual(failed["final_repair_summary"]["attempted_count"], 1)
        self.assertEqual(failed["final_repair_summary"]["unresolved_symbols"], ["fdb_kv_set"])
        self.assertTrue(resolved["checks"]["generation_diagnostics_clear"])
        self.assertEqual(resolved["final_repair_summary"]["resolved_symbols"], ["fdb_kv_set"])

