    def test_module_generation_records_unresolved_function_without_interrupting(self):
        functions = [
            {
                "name": "ok_value", "decl_kind": "definition", "translation_status": "translated",
                "file": "src/core.c", "line": 3, "return_type": "int", "params": [],
                "body_kind": "return_number", "body_value": "7",
            },
            {
                "name": "hard_value", "decl_kind": "definition", "translation_status": "unsupported",
                "unsupported_reason": "control flow pattern not recognized by the source-driven translator",
                "file": "src/core.c", "line": 8, "return_type": "int", "params": [],
                "body_kind": "unsupported", "raw_body": "if (x) { return 1; } return 0;",
            },
        ]
        module_rs, translated_count, rendered_functions, diagnostics = _render_module("core", functions, [], [], {})
        self.assertIn("pub fn ok_value()", module_rs)
        self.assertNotIn("pub fn hard_value()", module_rs)
        self.assertEqual(translated_count, 1)
        self.assertEqual([item["name"] for item in rendered_functions], ["ok_value"])
        self.assertEqual(diagnostics[0]["symbol"], "hard_value")
        self.assertFalse(diagnostics[0]["resolved"])
        self.assertEqual(summarize_final_repair(diagnostics)["unresolved_symbols"], ["hard_value"])

