    def test_incremental_regeneration_preserves_unrelated_hashes(self):
        with tempfile.TemporaryDirectory() as temp:
            root = Path(temp); inputs = _inputs(root / "trace"); project = _project(root)
            (project / "src" / "kvdb.rs").write_text("pub fn marker() -> usize { 1 }\n")
            before = snapshot_tree(project)
            def regenerate(candidate, _closure):
                path = candidate / "src" / "kvdb.rs"; path.write_text(path.read_text().replace("{ 1 }", "{ 2 }"))
            report = incremental_regenerate(project, root / "evidence", inputs, "kvdb", regenerate, build=True)
            after = snapshot_tree(project)
        self.assertTrue(report["passed"])
        self.assertEqual(report["changed_files"], ["src/kvdb.rs"])
        self.assertEqual(before["src/lib.rs"], after["src/lib.rs"])

    def test_incremental_unknown_target_and_boundary_violation_roll_back(self):
        with tempfile.TemporaryDirectory() as temp:
            root = Path(temp); inputs = _inputs(root / "trace"); project = _project(root)
            before = snapshot_tree(project)
            with self.assertRaises(GenerationBlocked):
                incremental_regenerate(project, root / "evidence", inputs, "missing", lambda *_: None, build=False)
            def violate(candidate, _closure):
                path = candidate / "src" / "lib.rs"; path.write_text(path.read_text() + "\npub fn changed() {}\n")
            (project / "src" / "kvdb.rs").write_text("pub fn marker() -> usize { 1 }\n")
            stable = snapshot_tree(project)
            with self.assertRaises(GenerationBlocked):
                incremental_regenerate(project, root / "evidence", inputs, "kvdb", violate, build=False)
            self.assertEqual(before, {k: v for k, v in stable.items() if k != "src/kvdb.rs"})
            self.assertEqual(stable, snapshot_tree(project))

    def test_incremental_result_is_deterministic_for_same_parent(self):
        def run_once(root):
            inputs = _inputs(root / "trace"); project = _project(root); (project / "src" / "kvdb.rs").write_text("pub fn marker() -> usize { 1 }\n")
            def regenerate(candidate, _closure):
                path = candidate / "src" / "kvdb.rs"; path.write_text("pub fn marker() -> usize { 2 }\n")
            return incremental_regenerate(project, root / "evidence", inputs, "kvdb", regenerate, build=False)
        with tempfile.TemporaryDirectory() as first, tempfile.TemporaryDirectory() as second:
            a = run_once(Path(first)); b = run_once(Path(second))
        for field in ("target_module", "direct_dependencies", "allowed_closure", "allowed_files", "changed_files", "out_of_bounds"):
            self.assertEqual(a[field], b[field])


