    def test_unsafe_metric_is_node_based_and_strict(self):
        with tempfile.TemporaryDirectory() as temp:
            project = Path(temp); (project / "src").mkdir()
            (project / "src" / "lib.rs").write_text("unsafe fn raw() {}\nfn safe() {}\n")
            failed = audit_unsafe(project)
            key = failed["occurrences"][0]["id"]
            still_failed = audit_unsafe(project, {key: {"necessity": "ffi", "safety_invariant": "valid pointer", "boundary": "raw"}})
            (project / "src" / "lib.rs").write_text("unsafe fn raw() {}\n" + "\n".join(f"fn safe{i}() {{}}" for i in range(10)))
            key = audit_unsafe(project)["occurrences"][0]["id"]
            passed = audit_unsafe(project, {key: {"necessity": "ffi", "safety_invariant": "valid pointer", "boundary": "raw"}})
        self.assertFalse(failed["passed"])
        self.assertFalse(still_failed["passed"])
        self.assertTrue(passed["passed"])
        self.assertLess(passed["ratio"], 0.10)

